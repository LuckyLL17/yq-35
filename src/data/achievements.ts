import type { Achievement, AchievementContext } from '@/types';

function bestScore(ctx: AchievementContext, id: string): number {
  return ctx.records[id as keyof typeof ctx.records]?.bestScore ?? 0;
}

const isHigherBetter: Record<string, boolean> = {
  reaction: false,
  'number-memory': true,
  typing: true,
  aim: false,
  chimp: true,
  'color-vision': true,
  'sequence-memory': true,
  stroop: true,
  'math-speed': true,
};

function scoreBetterThan(ctx: AchievementContext, id: string, threshold: number): boolean {
  const best = bestScore(ctx, id);
  if (best === 0) return false;
  return isHigherBetter[id] ? best >= threshold : best <= threshold;
}

